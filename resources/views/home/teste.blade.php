            <p>Sempre que trabalhar com metas, busque fazer projeções pessimistas, ou você se frustrará com facilidade. Caso a estratégia não esteja funcionando, vá mudando e adaptando de forma que fique mais próxima da realidade.
                <br>
            </p>
            <h2><strong>12. Converse com os melhores clientes</strong><br></h2>
            <p>Essa é uma velha estratégia do comércio, porém que raramente falha. Ligue para os clientes com maior recorrência de compra no seu negócio e avise sobre os novos produtos que chegaram à loja e sobre as promoções que eles poderão aproveitar.
                <br>
            </p>
            <p>Se possível, ofereça um desconto exclusivo, como gratidão pelo longo tempo de confiança, e também como forma de fazê-lo voltar à loja.
                <br>
            </p>
